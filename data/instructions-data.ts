// Helper function to get instruction by type and year
export function getInstructionByTypeAndYear(type: string, year: string) {
  if (!instructionsData[type as keyof typeof instructionsData]) {
    return null
  }

  const instruction = instructionsData[type as keyof typeof instructionsData].items.find((item) => item.id === year)

  return instruction || null
}

// Helper function to get all instruction types
export function getAllInstructionTypes() {
  return Object.keys(instructionsData)
}

// Helper function to get all years for a specific type
export function getYearsForType(type: string) {
  if (!instructionsData[type as keyof typeof instructionsData]) {
    return []
  }

  return instructionsData[type as keyof typeof instructionsData].items.map((item) => item.id)
}
