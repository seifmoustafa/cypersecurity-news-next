// Helper function to get all news items in a flat array
export function getAllNews() {
  const allNews: any[] = []
  Object.keys(newsData).forEach((category) => {
    newsData[category as keyof typeof newsData].forEach((item) => {
      allNews.push(item)
    })
  })
  return allNews
}

// Get featured news
export function getFeaturedNews() {
  return getAllNews().filter((item) => item.featured)
}

// Get latest news
export function getLatestNews(count = 5) {
  return getAllNews()
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, count)
}

// Get news by category
export function getNewsByCategory(category: string) {
  return newsData[category as keyof typeof newsData] || []
}

// Get news by ID
export function getNewsById(id: string) {
  return getAllNews().find((item) => item.id === id)
}
