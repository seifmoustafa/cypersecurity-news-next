export function getAllDefinitions() {
  const allDefinitions: any[] = []
  Object.keys(definitionsData).forEach((category) => {
    definitionsData[category as keyof typeof definitionsData].forEach((item) => {
      allDefinitions.push(item)
    })
  })
  return allDefinitions
}

export function getDefinitionsByCategory(category: string) {
  return definitionsData[category as keyof typeof definitionsData] || []
}

export function getDefinitionById(id: string) {
  return getAllDefinitions().find((item) => item.id === id)
}
